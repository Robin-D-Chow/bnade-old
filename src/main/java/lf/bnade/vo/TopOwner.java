package lf.bnade.vo;

public class TopOwner {
	private int aucQuantity;
	private String owner;
	private long total;

	public int getAucQuantity() {
		return aucQuantity;
	}

	public void setAucQuantity(int aucQuantity) {
		this.aucQuantity = aucQuantity;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

}
