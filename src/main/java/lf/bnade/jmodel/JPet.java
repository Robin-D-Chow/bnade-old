package lf.bnade.jmodel;


public class JPet {
	private String name;
	private String icon;
	private JPetStats stats;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public JPetStats getStats() {
		return stats;
	}

	public void setStats(JPetStats stats) {
		this.stats = stats;
	}

}